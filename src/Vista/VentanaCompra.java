    private void tblPlatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPlatosMouseClicked
        // TODO add your handling code here:
        
        int fila= tblPlatos.getSelectedRow();
        if(fila<0){
            JOptionPane.showMessageDialog(null, "De clic sobre la fila que tiene el platillo deseado");
        }
        else{
            //Tomando los valores de la fila seleccionada
            String platillo = tblPlatos.getValueAt(fila, 0).toString();
            double prec = (double) tblPlatos.getValueAt(fila, 1);
            String precio = String.valueOf(prec);
            int exist = (int) tblPlatos.getValueAt(fila, 2);
            String existencias = String.valueOf(exist);
            int unidad = 1; 
            int resta = exist-unidad; 
            aux= aux+unidad;
            String cant = String.valueOf(aux);  
            tblPlatos.setValueAt(resta, fila, 2);
            double sub = prec*aux ; 
            String subtot = String.valueOf(sub); 
            double descuento = prec * 0.1;
            String d = String.valueOf(descuento);
            double total = sub - descuento;
            String t = String.valueOf(total);
            //Pasando lo obtenido a textfields
            txtPltSelec.setText(platillo);
            txtPrecio.setText(precio);
            txtCant.setText(cant);
            txtSubTot.setText(subtot);
            txtDesc.setText(d);
            txtTot.setText(t); 
            
        }
    }//GEN-LAST:event_tblPlatosMouseClicked
